export default class Element {

    //al properties.
    constructor(atomicMass, atomicNumber, atomicRadius, boilingPoint, bondingType, cpkHexColor, density, electronAffinity, electronegativity, electronicConfiguration,
        groupBlock, ionRadius, ionizationEnergy, meltingPoint, name, oxidationStates, standardState, symbol, vanDelWaalsRadius, yearDiscovered) {

        this.atomicMass = atomicMass;
        this.atomicNumber = atomicNumber;
        this.atomicRadius = atomicRadius;
        this.boilingPoint = boilingPoint;
        this.bondingType = bondingType;
        this.cpkHexColor = cpkHexColor;
        this.density = density;
        this.electronAffinity = electronAffinity;
        this.electronegativity = electronegativity;
        this.electronicConfiguration = electronicConfiguration;
        this.groupBlock = groupBlock;
        this.ionRadius = ionRadius;
        this.ionizationEnergy = ionizationEnergy;
        this.meltingPoint = meltingPoint;
        this.name = name;
        this.oxidationStates = oxidationStates;
        this.standardState = standardState;
        this.symbol = symbol;
        this.vanDelWaalsRadius = vanDelWaalsRadius;
        this.yearDiscovered = yearDiscovered;
    }

}